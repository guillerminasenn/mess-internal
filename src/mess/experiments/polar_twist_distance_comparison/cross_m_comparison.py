"""Cross-M mixing comparison for uniform vs LP angular/euclidean MESS variants."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Dict, List, Optional

import matplotlib.pyplot as plt
import numpy as np

from mess.algorithms.effective_sample_size import estimate_effective_sample_size
from mess.experiments.common.plotting_utils import apply_publication_style, save_figure
from mess.experiments.polar_twist_distance_comparison.config import ExperimentConfig, build_context


def _msjd_per_dim(samples: np.ndarray) -> np.ndarray:
    if samples.shape[0] < 2:
        return np.full(samples.shape[1], np.nan, dtype=float)
    jumps = np.diff(samples, axis=0)
    return np.mean(jumps * jumps, axis=0)


def _chain_metrics(chain: np.ndarray, burn_in: int, thin: int, max_lag: int) -> Dict[str, float]:
    samples = chain[int(burn_in) :: int(thin)]
    if samples.shape[0] < 4:
        return {
            "ess_x1": float("nan"),
            "ess_x2": float("nan"),
            "ess_mean": float("nan"),
            "msjd_x1": float("nan"),
            "msjd_x2": float("nan"),
            "msjd_mean": float("nan"),
        }

    ess_vec = np.array(
        [
            float(estimate_effective_sample_size(samples[:, j], max_lag=int(max_lag)))
            for j in range(samples.shape[1])
        ],
        dtype=float,
    )
    msjd_vec = _msjd_per_dim(samples)

    return {
        "ess_x1": float(ess_vec[0]) if ess_vec.shape[0] > 0 else float("nan"),
        "ess_x2": float(ess_vec[1]) if ess_vec.shape[0] > 1 else float("nan"),
        "ess_mean": float(np.nanmean(ess_vec)),
        "msjd_x1": float(msjd_vec[0]) if msjd_vec.shape[0] > 0 else float("nan"),
        "msjd_x2": float(msjd_vec[1]) if msjd_vec.shape[0] > 1 else float("nan"),
        "msjd_mean": float(np.nanmean(msjd_vec)),
    }


def _latest_match_with_niters(root: Path, pattern: str, expected_niters: int) -> Optional[Path]:
    candidates = sorted(root.glob(pattern), key=lambda p: p.stat().st_mtime, reverse=True)
    for candidate in candidates:
        try:
            with np.load(candidate) as payload:
                n_iters = int(np.asarray(payload.get("n_iters", -1)).reshape(-1)[0])
            if n_iters == int(expected_niters):
                return candidate
        except Exception:
            continue
    return None


def _uniform_chain_path(repo_root: Path, data_id: str, m: int, expected_niters: int) -> Optional[Path]:
    base = repo_root / "estimations" / "polar_twist_mcmc_comparison" / data_id / "sweep"
    match = _latest_match_with_niters(base, f"run_h*/chain_mess_M{int(m)}.npz", expected_niters)
    if match is not None:
        return match
    fallback = repo_root / "estimations" / "polar_twist_mcmc_comparison"
    return _latest_match_with_niters(
        fallback,
        f"data_h*/sweep/run_h*/chain_mess_M{int(m)}.npz",
        expected_niters,
    )


def _lp_chain_path(repo_root: Path, data_id: str, m: int, variant: str, expected_niters: int) -> Optional[Path]:
    base = repo_root / "estimations" / "polar_twist_distance_comparison" / data_id / "fixed"
    filename = f"chain_mess_{variant}_M{int(m)}.npz"
    match = _latest_match_with_niters(base, f"run_h*/{filename}", expected_niters)
    if match is not None:
        return match
    fallback = repo_root / "estimations" / "polar_twist_distance_comparison"
    return _latest_match_with_niters(
        fallback,
        f"data_h*/fixed/run_h*/{filename}",
        expected_niters,
    )


def _write_tex_table(path: Path, title: str, rows: List[Dict[str, object]], metric_prefix: str) -> None:
    lines = []
    lines.append("% Auto-generated by cross_m_comparison.py")
    lines.append("\\begin{table}[t]")
    lines.append("\\centering")
    lines.append(f"\\caption{{{title}}}")
    lines.append("\\begin{tabular}{l l r r r}")
    lines.append("\\hline")
    lines.append("M & Variant & x1 & x2 & mean \\\\")
    lines.append("\\hline")
    for row in rows:
        m = int(row["M"])
        variant = str(row["variant"])
        x1 = float(row[f"{metric_prefix}_x1"])
        x2 = float(row[f"{metric_prefix}_x2"])
        mean = float(row[f"{metric_prefix}_mean"])
        lines.append(f"{m} & {variant} & {x1:.4f} & {x2:.4f} & {mean:.4f} \\\\")
    lines.append("\\hline")
    lines.append("\\end{tabular}")
    lines.append("\\end{table}")
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as handle:
        handle.write("\n".join(lines) + "\n")


def _plot_grouped(rows: List[Dict[str, object]], out_path: Path, metric_prefix: str, title: str) -> Path:
    variant_order = ["uniform", "lp_angular", "lp_euclidean"]
    variant_labels = {
        "uniform": "uniform",
        "lp_angular": "LP angular",
        "lp_euclidean": "LP euclidean",
    }
    variant_colors = {
        "uniform": "tab:blue",
        "lp_angular": "tab:orange",
        "lp_euclidean": "tab:green",
    }

    m_values = sorted({int(r["M"]) for r in rows})
    comp_keys = ["x1", "x2", "mean"]

    lookup: Dict[tuple[int, str], Dict[str, object]] = {}
    for row in rows:
        lookup[(int(row["M"]), str(row["variant"]))] = row

    x = np.arange(len(m_values), dtype=float)
    width = 0.24

    fig, axes = plt.subplots(1, 3, figsize=(15.5, 4.6), sharex=True, constrained_layout=True)
    for ax, comp in zip(axes, comp_keys):
        for i, variant in enumerate(variant_order):
            vals = []
            for m in m_values:
                row = lookup.get((int(m), variant))
                vals.append(float(row.get(f"{metric_prefix}_{comp}", np.nan)) if row is not None else np.nan)
            ax.bar(x + (i - 1) * width, vals, width=width, label=variant_labels[variant], color=variant_colors[variant])

        ax.set_title(comp)
        ax.set_xticks(x)
        ax.set_xticklabels([str(m) for m in m_values])
        ax.set_xlabel("M")
        ax.grid(alpha=0.2, axis="y")

    axes[0].set_ylabel(metric_prefix.upper())
    handles, labels = axes[0].get_legend_handles_labels()
    fig.legend(handles, labels, loc="upper center", bbox_to_anchor=(0.5, 1.08), ncol=3, frameon=False)
    fig.suptitle(title)

    return save_figure(fig, out_path, dpi=350)


def _plot_mess_vs_m_by_variant(
    rows: List[Dict[str, object]],
    out_path: Path,
    key_x1: str,
    key_x2: str,
    key_mean: str,
    title: str,
    y_label: str,
) -> Path:
    variant_order = ["uniform", "lp_angular", "lp_euclidean"]
    variant_labels = {
        "uniform": "uniform",
        "lp_angular": "LP angular",
        "lp_euclidean": "LP euclidean",
    }

    by_variant: Dict[str, List[Dict[str, object]]] = {}
    for row in rows:
        by_variant.setdefault(str(row["variant"]), []).append(row)

    fig, axes = plt.subplots(1, 3, figsize=(14.2, 4.2), sharex=True, constrained_layout=True)
    panel_specs = [(key_x1, r"$x_1$"), (key_x2, r"$x_2$"), (key_mean, "mean")]
    for ax, (metric_key, subtitle) in zip(axes, panel_specs):
        for variant in variant_order:
            vr = sorted(by_variant.get(variant, []), key=lambda item: int(item["M"]))
            if not vr:
                continue
            m_vals = np.asarray([int(item["M"]) for item in vr], dtype=int)
            vals = np.asarray([float(item.get(metric_key, np.nan)) for item in vr], dtype=float)
            ax.plot(m_vals, vals, marker="o", linewidth=1.3, label=variant_labels[variant])
        ax.set_title(subtitle)
        ax.set_xlabel("M")
        ax.grid(alpha=0.2)

    axes[0].set_ylabel(y_label)
    handles, labels = axes[0].get_legend_handles_labels()
    fig.legend(handles, labels, loc="upper center", bbox_to_anchor=(0.5, 1.08), ncol=3, frameon=False)
    fig.suptitle(title)
    return save_figure(fig, out_path, dpi=350)


def run(config: Optional[ExperimentConfig] = None, m_values: Optional[List[int]] = None) -> Dict[str, object]:
    cfg = config or ExperimentConfig()
    ctx = build_context(cfg)
    repo_root = Path(ctx["repo_root"])
    data_id = str(ctx["data_id"])

    m_values = m_values or [5, 10, 20, 50]
    m_values = [int(m) for m in m_values if int(m) > 1]

    rows: List[Dict[str, object]] = []
    missing: List[Dict[str, object]] = []

    for m in m_values:
        for variant in ("uniform", "lp_angular", "lp_euclidean"):
            if variant == "uniform":
                path = _uniform_chain_path(repo_root, data_id, m, expected_niters=int(cfg.n_iters))
            else:
                path = _lp_chain_path(repo_root, data_id, m, variant, expected_niters=int(cfg.n_iters))

            if path is None or not path.exists():
                missing.append({"M": m, "variant": variant})
                continue

            with np.load(path) as payload:
                chain = np.asarray(payload["chain"], dtype=float)

            metrics = _chain_metrics(chain, cfg.burn_in, cfg.thin, cfg.max_lag)
            rows.append(
                {
                    "M": int(m),
                    "variant": variant,
                    "path": str(path.resolve()),
                    **metrics,
                }
            )

    rows = sorted(rows, key=lambda r: (int(r["M"]), str(r["variant"])))

    out_tables = ctx["reports_dir"] / "tables"
    out_figs = ctx["reports_dir"] / "figures" / "cross_m"
    out_tables.mkdir(parents=True, exist_ok=True)
    out_figs.mkdir(parents=True, exist_ok=True)

    json_path = out_tables / "cross_m_metrics_summary.json"
    with open(json_path, "w", encoding="utf-8") as handle:
        json.dump({"rows": rows, "missing": missing}, handle, indent=2)

    ess_tex = out_tables / "cross_m_ess_table.tex"
    msjd_tex = out_tables / "cross_m_msjd_table.tex"
    _write_tex_table(ess_tex, "ESS comparison across M and transition-matrix variants", rows, "ess")
    _write_tex_table(msjd_tex, "MSJD comparison across M and transition-matrix variants", rows, "msjd")

    apply_publication_style()
    ess_fig = _plot_grouped(
        rows,
        out_figs / "cross_m_ess_grouped.png",
        metric_prefix="ess",
        title="ESS by M and transition-matrix variant",
    )
    msjd_fig = _plot_grouped(
        rows,
        out_figs / "cross_m_msjd_grouped.png",
        metric_prefix="msjd",
        title="MSJD by M and transition-matrix variant",
    )

    energy_ok = all(np.isfinite(float(r.get("ess_mean_per_energy_lik", np.nan))) for r in rows)
    wallclock_ok = all(np.isfinite(float(r.get("ess_mean_per_parallel_lik_step", np.nan))) for r in rows)

    mess_raw_fig = _plot_mess_vs_m_by_variant(
        rows,
        out_figs / "cross_m_mess_ess_vs_M_raw.png",
        key_x1="ess_x1",
        key_x2="ess_x2",
        key_mean="ess_mean",
        title="MESS ESS vs M by transition-matrix variant",
        y_label="ESS",
    )

    mess_energy_fig = None
    if energy_ok:
        mess_energy_fig = _plot_mess_vs_m_by_variant(
            rows,
            out_figs / "cross_m_mess_ess_vs_M_per_energy_lik_eval.png",
            key_x1="ess_x1_per_energy_lik",
            key_x2="ess_x2_per_energy_lik",
            key_mean="ess_mean_per_energy_lik",
            title="MESS ESS per likelihood evaluation (energy cost) vs M",
            y_label="ESS / likelihood evaluation",
        )

    mess_wallclock_fig = None
    if wallclock_ok:
        mess_wallclock_fig = _plot_mess_vs_m_by_variant(
            rows,
            out_figs / "cross_m_mess_ess_vs_M_per_parallel_lik_step.png",
            key_x1="ess_x1_per_parallel_lik_step",
            key_x2="ess_x2_per_parallel_lik_step",
            key_mean="ess_mean_per_parallel_lik_step",
            title="MESS ESS per parallel likelihood step vs M",
            y_label="ESS / parallel likelihood step",
        )

    availability_path = out_tables / "cross_m_mess_ess_vs_M_availability.json"
    with open(availability_path, "w", encoding="utf-8") as handle:
        json.dump(
            {
                "energy_normalization_available": bool(energy_ok),
                "parallel_step_normalization_available": bool(wallclock_ok),
                "reason_if_unavailable": (
                    "missing mess_subiters_per_iter in one or more MESS chain artifacts"
                    if not (energy_ok and wallclock_ok)
                    else "all required normalized metrics available"
                ),
            },
            handle,
            indent=2,
        )

    return {
        "rows": len(rows),
        "missing": missing,
        "json": str(json_path.resolve()),
        "ess_tex": str(ess_tex.resolve()),
        "msjd_tex": str(msjd_tex.resolve()),
        "ess_fig": str(ess_fig.resolve()),
        "msjd_fig": str(msjd_fig.resolve()),
        "mess_raw_fig": str(mess_raw_fig.resolve()),
        "mess_energy_fig": str(mess_energy_fig.resolve()) if mess_energy_fig else None,
        "mess_wallclock_fig": str(mess_wallclock_fig.resolve()) if mess_wallclock_fig else None,
        "mess_availability": str(availability_path.resolve()),
    }


def main() -> None:
    summary = run()
    print("\nCross-M comparison summary:")
    print(f"- Rows: {summary['rows']}")
    print(f"- Missing cells: {len(summary['missing'])}")
    print(f"- JSON: {summary['json']}")
    print(f"- ESS TeX: {summary['ess_tex']}")
    print(f"- MSJD TeX: {summary['msjd_tex']}")
    print(f"- ESS figure: {summary['ess_fig']}")
    print(f"- MSJD figure: {summary['msjd_fig']}")


if __name__ == "__main__":
    main()
